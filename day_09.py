# Type Casting
# Coversion of one data type into another data type
# str, int, float, list, tuple, dict, set, ord, hex, oct

# Explixit casting => which human did
a = "1" #This is in a sting data type
# a = 1
b = "2" #This is in a sting data type
# b = 2
print(int(a) + int(b)) #here both string typecast in int to +

# Implicit TypeCasting => which is builtin
c = 1.9 #float
d = 8 #int
print(c + d) #This show float instead of integer